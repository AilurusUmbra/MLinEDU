# print_test.py

# 宣告變數 a 和 b，並賦值給 a 和 b
a = 5
b = 3

# 印出 a 和 b 的值
print(a, b)

# 印出字串 '-' 五次
print('-'*5)

# a**b 是 a 的 b 次方
print(a+b, a*b, a**b)

# 印出字串 '==' 五次
print('=='*5)

# a/b 是除法，a//b 除法取整數商，a%b 除法取餘數
print(a/b, a//b, a%b)

# 每一個 print() 指令預設都會印出換行符號，所以即使沒有要印的變數也會換一行
print()

# 要 print 的每個變數之間用 '_' 分隔
print(a, b, a+b, sep='_')
